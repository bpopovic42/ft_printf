#include "ft_printf.h"

int			ft_printf_is_fspecif(int c)
{
	return (c == 's' || c == 'S' || c == 'p' || c == 'd' || c == 'D' || c == 'i'
			|| c == 'o' || c == 'O' || c == 'u' || c == 'U' || c == 'x'
				|| c == 'X' || c == 'c' || c == 'C' || c == 'f' || c == 'F'
					|| c == 'g' || c == 'G' || c == 'e'
						|| c == 'E' || c == '%');
}

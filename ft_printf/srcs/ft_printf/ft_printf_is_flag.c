#include "ft_printf.h"

int		ft_printf_is_flag(char c)
{
	return (ft_isdigit(c) || c == '-' || c == '+' || c == '#' || c == '.'
		|| c == 'h' || c == 'j' || c == 'l' || c == 'z' || c == ' '
			|| c == '%' || c == '*');
}
